"""
Naming conventions
NOTE:
1. Variable names are case-sensitive.
2. Variable names must start with a letter or an underscore.
3. Variable names can only contain alpha-numeric characters and underscores (A-z, 0-9, and _ ).
"""
# Legal variable names
myvar = "Hello World"
my_var = "Hello World"
_my_var = "Hello World"
myVar = "Hello World"
MYVAR = "Hello World"
myvar2 = "Hello World"
my_var2 = "Hello World"
_my_var2 = "Hello World"
myVar2 = "Hello World"
MYVAR2 = "Hello World"

# Illegal variable names
# 2myvar = "Hello World"
# my-var = "Hello World"
# my var = "Hello World"

"""
Multiple words variable naming conventions
RECOMMENDATION:
1. Snake Case: Use underscores to separate words.
"""
# Camel Case
myVariableName = "Hello World"

# Pascal Case
MyVariableName = "Hello World"

# Snake Case
my_variable_name = "Hello World"